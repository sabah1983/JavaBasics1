package com.syntax.class15;

public class Calculator {
	
	// define methods to calculate add, sub, div, mult
	
	
	//created method with name add that accepts
		//two parameters of integer type
	void add(int a, int b) {
		System.out.println(a+b);
	}
	//created method with name sub that accepts
	//two parameters of integer type
	void sub(int a, int b) {
		System.out.println(a-b);
	}
	//created method with name mult that accepts
	//two parameters of integer type
	void mult(int a, int b) {
		System.out.println(a*b);
	}
	//created method with name div that accepts
	//two parameters of double type
	void div(double a, double b) {
		System.out.println(a/b);
	}
	
	
	

}
