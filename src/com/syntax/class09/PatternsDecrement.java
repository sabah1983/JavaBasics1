package com.syntax.class09;

public class PatternsDecrement {

	public static void main(String[] args) {
		// print from 5to 1
		
		for(int i=5; i>=1; i--) {
			for(int y=5; y>=1; y--) {
				System.out.print(y);
			}
			System.out.println();
		}
          System.out.println("----------");

       for(int z=5; z>=1; z--) {
    	   for(int h=5; h>=1; h--) {
    		   System.out.print(z);
    	   }
    	   System.out.println();
       }
	}

}
