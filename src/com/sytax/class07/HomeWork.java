package com.sytax.class07;

public class HomeWork {

	public static void main(String[] args) {
		// print numbers from 1 to 100 in 1 line with space
		int num=1;
		while(num<=100) {
			System.out.print(num+" ");
			num++;
		}
		
		int num1=100;
		while(num1>=1) {
			System.out.println(num1);
			num1--;
		}

	}

}
