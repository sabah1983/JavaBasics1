package com.syntax.class05;

public class WeekDay {

	public static void main(String[] args) {
	int day=1;
	if (day==1 || day==5) {
		System.out.println("Is weekday");
	}else if (day>=6 && day==7) {
		System.out.println("is weekend");
	}else {
		System.out.println("Invalid day");
	}
	
	

	}

}
